char* MALLOC_get_memory(size_t s)
{
  char* adreca;

  adreca = (char*) malloc(s);
  if (adreca == NULL)
  {
    fprintf(stderr,"Out of Memory\n");
    exit(1);
  }
  return(adreca);
}

void MALLOC_free_memory(char *a)
{
  free(a);
}

